/*----------------------------------------------------------
*
*   MACRO NAME:
*       GRM_nibble_lower - Nibble Lower
*
*   DESCRIPTION:
*       Returns the lower nibble of a byte.
*
*       uint8 GRM_nibble_lower
*               (
*               _val //Byte to retrieve nibble
*               )
*
----------------------------------------------------------*/
#define GRM_nibble_lower(_val) (_val & 0x0F)

/*----------------------------------------------------------
*
*   MACRO NAME:
*       GRM_nibble_upper - Nibble Upper
*
*   DESCRIPTION:
*       Returns the upper nibble of a byte.
*
*       uint8 GRM_nibble_upper
*               (
*               _val //Byte to retrieve nibble
*               )
*
----------------------------------------------------------*/
#define GRM_nibble_upper(_val) (_val >> 4)

